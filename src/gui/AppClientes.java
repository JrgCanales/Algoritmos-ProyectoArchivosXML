    private void btnActualizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarMouseClicked
        // TODO add your handling code here:
        
        String id = txtId.getText();
        String nombre = txtNombre.getText();
        String identidad = txtIdentidad.getText();
        String telefono = txtTelefono.getText();
        String ciudad = txtCiudad.getText();
        String correo = txtCorreo.getText();
        String edad = txtEdad.getText();
        
        camposVacios(id, nombre, identidad, telefono, ciudad, correo, edad);
        
        if(camposVacios==0){
            try {
                Cliente cliente = new Cliente();
                ClienteCRUD clienteCrud = new ClienteCRUD();
                
                cliente.setId(Integer.parseInt(id));
                cliente.setNombre(nombre);
                cliente.setIdentidad(identidad);
                cliente.setTelefono(telefono);
                cliente.setCiudad(ciudad);
                cliente.setCorreo(correo);
                cliente.setEdad(Integer.parseInt(edad));
                
                clienteCrud.actualizarCliente(cliente);
                
                controlesInicio();
                limpiarTxt();
                clienteCrud.listadoClientes(tblClientes);
                
            } catch (SAXException | IOException | TransformerException | ParserConfigurationException  ex) {
                Logger.getLogger(AppClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "No puede actualizar datos del Cliente, Complete los campos!");
        }
    }//GEN-LAST:event_btnActualizarMouseClicked
    
