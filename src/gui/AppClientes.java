    private void camposVacios(String id, String nombre, String identidad, String telefono, String ciudad, String correo, String edad){
        camposVacios=0;
        
        if(!id.isEmpty()){
            if(!nombre.isEmpty()){
                if(!identidad.isEmpty()){
                    if(!telefono.isEmpty()){
                        if(!ciudad.isEmpty()){
                            if(!correo.isEmpty()){
                                if(!edad.isEmpty()){
                                    //sin campos vacios
                                }else{
                                    camposVacios++;
                                }
                            }else{
                                camposVacios++;
                            }
                        }else{
                            camposVacios++;
                        }
                    }else{
                        camposVacios++;
                    }
                }else{
                    camposVacios++;
                }
            }else{
                camposVacios++;
            }
        }else{
            camposVacios++;
        }
    }
    
    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        try {
            // TODO add your handling code here:
            
            Cliente client = new Cliente();
            ClienteCRUD clienteCrud = new ClienteCRUD();
            
            client.setId(Integer.parseInt(txtBuscarId.getText()));
            clienteCrud.buscarCliente(client);
            
            txtId.setText(Integer.toString(client.getId()));
            txtNombre.setText(client.getNombre());
            txtIdentidad.setText(client.getIdentidad());
            txtTelefono.setText(client.getTelefono());
            txtCiudad.setText(client.getCiudad());
            txtCorreo.setText(client.getCorreo());
            txtEdad.setText(Integer.toString(client.getEdad()));
            
            if(!txtIdentidad.getText().isEmpty()){
                
                txtId.setEnabled(false);
                txtNombre.setEnabled(true);
                txtIdentidad.setEnabled(false);
                txtTelefono.setEnabled(true);
                txtCiudad.setEnabled(true);
                txtCorreo.setEnabled(true);
                txtEdad.setEnabled(true);
        
                btnNuevo.setEnabled(false);
                btnGuardar.setEnabled(false);
                btnCancelar.setEnabled(true);
                btnActualizar.setEnabled(true);
                btnEliminar.setEnabled(true);
                btnBuscar.setEnabled(true);
            }else{
                controlesInicio();
                limpiarTxt();
            }
            
        } catch (SAXException | IOException  ex) {
            Logger.getLogger(AppClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtBuscarId.setText("");
        
    }//GEN-LAST:event_btnBuscarMouseClicked

